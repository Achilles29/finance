-- Repair MINYAK GORENG daily-recon adjustments posted during the broken cross-profile FIFO logic.
-- Problem:
-- - Adjustment per profile consumed FIFO lots from other profile_key.
-- - Some monthly stock rows were not reduced because only the target adjustment profile was synced.
--
-- Scope:
-- - Division KITCHEN (division_id=3), material MINYAK GORENG (material_id=143), item_id=198, content_uom_id=9.
-- - Adjustment test rows captured on 2026-07-11 from /inventory/stock/daily-recon/division.
--
-- Review the id list before running if more adjustments have been posted after this file was created.

CREATE TABLE IF NOT EXISTS zz_backup_20260711c_mg_inv_stock_adjustment AS
SELECT *
FROM inv_stock_adjustment
WHERE id IN (1187,1189,1191,1193,1195,1197,1199,1217,1219,1221,1225,1227,1229,1231,1233,1235,1237,1239,1241,1243,1245,1249,1251);

CREATE TABLE IF NOT EXISTS zz_backup_20260711c_mg_inv_stock_adjustment_line AS
SELECT *
FROM inv_stock_adjustment_line
WHERE adjustment_id IN (1187,1189,1191,1193,1195,1197,1199,1217,1219,1221,1225,1227,1229,1231,1233,1235,1237,1239,1241,1243,1245,1249,1251);

CREATE TABLE IF NOT EXISTS zz_backup_20260711c_mg_inv_stock_movement_log AS
SELECT *
FROM inv_stock_movement_log
WHERE ref_table = 'inv_stock_adjustment'
  AND ref_id IN (1187,1189,1191,1193,1195,1197,1199,1217,1219,1221,1225,1227,1229,1231,1233,1235,1237,1239,1241,1243,1245,1249,1251);

CREATE TABLE IF NOT EXISTS zz_backup_20260711c_mg_inv_material_fifo_issue_log AS
SELECT il.*
FROM inv_material_fifo_issue_log il
JOIN inv_stock_adjustment_line al
  ON il.id IN (al.waste_issue_id, al.spoil_issue_id, al.process_loss_issue_id, al.variance_issue_id)
WHERE al.adjustment_id IN (1187,1189,1191,1193,1195,1197,1199,1217,1219,1221,1225,1227,1229,1231,1233,1235,1237,1239,1241,1243,1245,1249,1251);

CREATE TABLE IF NOT EXISTS zz_backup_20260711c_mg_inv_material_fifo_issue_line AS
SELECT ill.*
FROM inv_material_fifo_issue_line ill
JOIN zz_backup_20260711c_mg_inv_material_fifo_issue_log il ON il.id = ill.issue_id;

CREATE TABLE IF NOT EXISTS zz_backup_20260711c_mg_inv_material_fifo_lot AS
SELECT DISTINCT l.*
FROM inv_material_fifo_lot l
JOIN zz_backup_20260711c_mg_inv_material_fifo_issue_line ill ON ill.lot_id = l.id;

CREATE TABLE IF NOT EXISTS zz_backup_20260711c_mg_inv_division_monthly_stock AS
SELECT *
FROM inv_division_monthly_stock
WHERE month_key = '2026-07-01'
  AND division_id = 3
  AND destination_type = 'KITCHEN'
  AND item_id = 198
  AND material_id = 143
  AND content_uom_id = 9;

CREATE TABLE IF NOT EXISTS zz_backup_20260711c_mg_inv_division_stock_opname AS
SELECT *
FROM inv_division_stock_opname
WHERE adjustment_id IN (1187,1189,1191,1193,1195,1197,1199,1217,1219,1221,1225,1227,1229,1231,1233,1235,1237,1239,1241,1243,1245,1249,1251);

START TRANSACTION;

CREATE TEMPORARY TABLE tmp_20260711c_mg_bad_adj (
  id BIGINT UNSIGNED PRIMARY KEY
);

INSERT INTO tmp_20260711c_mg_bad_adj (id)
VALUES
  (1187),(1189),(1191),(1193),(1195),(1197),(1199),
  (1217),(1219),(1221),(1225),(1227),(1229),(1231),
  (1233),(1235),(1237),(1239),(1241),(1243),(1245),
  (1249),(1251);

CREATE TEMPORARY TABLE tmp_20260711c_mg_bad_issue AS
SELECT DISTINCT issue_id
FROM (
  SELECT waste_issue_id AS issue_id
  FROM inv_stock_adjustment_line
  WHERE adjustment_id IN (SELECT id FROM tmp_20260711c_mg_bad_adj)
  UNION ALL
  SELECT spoil_issue_id AS issue_id
  FROM inv_stock_adjustment_line
  WHERE adjustment_id IN (SELECT id FROM tmp_20260711c_mg_bad_adj)
  UNION ALL
  SELECT process_loss_issue_id AS issue_id
  FROM inv_stock_adjustment_line
  WHERE adjustment_id IN (SELECT id FROM tmp_20260711c_mg_bad_adj)
  UNION ALL
  SELECT variance_issue_id AS issue_id
  FROM inv_stock_adjustment_line
  WHERE adjustment_id IN (SELECT id FROM tmp_20260711c_mg_bad_adj)
) x
WHERE issue_id IS NOT NULL AND issue_id > 0;

CREATE TEMPORARY TABLE tmp_20260711c_mg_lot_rollback AS
SELECT lot_id, ROUND(SUM(qty_out), 4) AS rollback_qty
FROM inv_material_fifo_issue_line
WHERE issue_id IN (SELECT issue_id FROM tmp_20260711c_mg_bad_issue)
GROUP BY lot_id;

-- Return FIFO quantities consumed by the bad adjustment documents.
UPDATE inv_material_fifo_lot l
JOIN tmp_20260711c_mg_lot_rollback rb ON rb.lot_id = l.id
SET l.qty_out = GREATEST(0, ROUND(l.qty_out - rb.rollback_qty, 4)),
    l.qty_balance = ROUND(l.qty_balance + rb.rollback_qty, 4),
    l.status = 'OPEN',
    l.updated_at = NOW();

-- Remove FIFO issue logs generated by those adjustment documents.
DELETE ill
FROM inv_material_fifo_issue_line ill
JOIN tmp_20260711c_mg_bad_issue bi ON bi.issue_id = ill.issue_id;

DELETE il
FROM inv_material_fifo_issue_log il
JOIN tmp_20260711c_mg_bad_issue bi ON bi.issue_id = il.id;

-- Remove stock movement generated by those adjustment documents.
DELETE ml
FROM inv_stock_movement_log ml
JOIN tmp_20260711c_mg_bad_adj ba ON ba.id = ml.ref_id
WHERE ml.ref_table = 'inv_stock_adjustment';

-- Clear daily-recon linkage to deleted adjustments.
UPDATE inv_division_stock_opname o
JOIN tmp_20260711c_mg_bad_adj ba ON ba.id = o.adjustment_id
SET o.adjustment_id = NULL;

-- Remove adjustment documents. Backup tables above keep a copy.
DELETE al
FROM inv_stock_adjustment_line al
JOIN tmp_20260711c_mg_bad_adj ba ON ba.id = al.adjustment_id;

DELETE h
FROM inv_stock_adjustment h
JOIN tmp_20260711c_mg_bad_adj ba ON ba.id = h.id;

-- Normalize legacy current-month lot metadata for this material.
UPDATE inv_material_fifo_lot l
JOIN mst_item i ON i.id = l.item_id
SET l.material_id = i.material_id,
    l.updated_at = NOW()
WHERE l.location_scope = 'DIVISION'
  AND l.division_id = 3
  AND l.destination_type = 'KITCHEN'
  AND l.item_id = 198
  AND (l.material_id IS NULL OR l.material_id = 0)
  AND i.material_id = 143;

CREATE TEMPORARY TABLE tmp_20260711c_mg_lot_balance AS
SELECT
  COALESCE(l.profile_key, '') AS profile_key,
  ROUND(SUM(l.qty_balance), 4) AS qty_content,
  ROUND(SUM(l.qty_balance * l.unit_cost), 2) AS total_value,
  ROUND(SUM(l.qty_balance * l.unit_cost) / NULLIF(SUM(l.qty_balance), 0), 6) AS avg_cost
FROM inv_material_fifo_lot l
LEFT JOIN mst_item i ON i.id = l.item_id
WHERE l.location_scope = 'DIVISION'
  AND l.division_id = 3
  AND l.destination_type = 'KITCHEN'
  AND COALESCE(l.material_id, i.material_id) = 143
  AND l.item_id = 198
  AND l.content_uom_id = 9
  AND l.status = 'OPEN'
  AND l.qty_balance > 0.0001
  AND l.receipt_date >= '2026-07-01'
  AND l.receipt_date <= '2026-07-31'
GROUP BY COALESCE(l.profile_key, '');

-- Make monthly stock follow current-month FIFO lots after rollback.
UPDATE inv_division_monthly_stock s
LEFT JOIN tmp_20260711c_mg_lot_balance b ON b.profile_key = COALESCE(s.profile_key, '')
SET s.closing_qty_content = COALESCE(b.qty_content, 0),
    s.closing_qty_buy = CASE
      WHEN COALESCE(s.profile_content_per_buy, 0) > 0
      THEN ROUND(COALESCE(b.qty_content, 0) / s.profile_content_per_buy, 4)
      ELSE s.closing_qty_buy
    END,
    s.avg_cost_per_content = COALESCE(b.avg_cost, 0),
    s.total_value = COALESCE(b.total_value, 0),
    s.last_movement_table = 'repair_sql',
    s.last_movement_id = NULL,
    s.last_movement_date = '2026-07-11',
    s.notes = LEFT(CONCAT(COALESCE(s.notes, ''), ' | Repair 2026-07-11c: rollback bad MINYAK GORENG daily-recon adjustment and sync from FIFO lot'), 255),
    s.updated_at = NOW()
WHERE s.month_key = '2026-07-01'
  AND s.division_id = 3
  AND s.destination_type = 'KITCHEN'
  AND s.item_id = 198
  AND s.material_id = 143
  AND s.content_uom_id = 9;

COMMIT;

-- Verification: stock vs FIFO should match per profile for July KITCHEN MINYAK GORENG.
SELECT
  s.profile_key,
  s.profile_name,
  ROUND(s.closing_qty_content, 4) AS monthly_qty,
  ROUND(COALESCE(b.qty_content, 0), 4) AS fifo_qty,
  ROUND(s.total_value, 2) AS monthly_value,
  ROUND(COALESCE(b.total_value, 0), 2) AS fifo_value
FROM inv_division_monthly_stock s
LEFT JOIN (
  SELECT
    COALESCE(l.profile_key, '') AS profile_key,
    SUM(l.qty_balance) AS qty_content,
    SUM(l.qty_balance * l.unit_cost) AS total_value
  FROM inv_material_fifo_lot l
  LEFT JOIN mst_item i ON i.id = l.item_id
  WHERE l.location_scope = 'DIVISION'
    AND l.division_id = 3
    AND l.destination_type = 'KITCHEN'
    AND COALESCE(l.material_id, i.material_id) = 143
    AND l.item_id = 198
    AND l.content_uom_id = 9
    AND l.status = 'OPEN'
    AND l.qty_balance > 0.0001
    AND l.receipt_date >= '2026-07-01'
    AND l.receipt_date <= '2026-07-31'
  GROUP BY COALESCE(l.profile_key, '')
) b ON b.profile_key = COALESCE(s.profile_key, '')
WHERE s.month_key = '2026-07-01'
  AND s.division_id = 3
  AND s.destination_type = 'KITCHEN'
  AND s.item_id = 198
  AND s.material_id = 143
  AND s.content_uom_id = 9
ORDER BY s.profile_name, s.profile_key;
